#include<iostream>
using namespace std;

int main(){
    /*
	1. 숫자 변수 a에 10을 저장하고 출력하는 코드를 작성하라.
	*/
	int a = 10;
	cout << "연습 문제 1.1. : " << a << endl;

    /*
    2. 문자열 변수 name에 "Alice"를 저장하고, "Hello, Alice!"를 출력하는 코드를 작성하라.
    */
	string name = "Alice";
	cout << "연습 문제 1.2. : Hello, " << name << "!" << endl;

    /*
    3. 변수 x에 숫자 5, 변수 y에 숫자 3.14를 저장한 후, 둘다 한줄에 출력하는 코드를 작성하라.
    */
    int x = 5;
	float y = 3.14;
	cout << "연습 문제 1.3. : " << x << ", " << y << endl;

    return 0;
}